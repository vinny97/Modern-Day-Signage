#!/usr/bin/env node
'use strict';

/**
 * Generate an idempotent "catch-up" SQL file that brings a Supabase/Postgres
 * database up to the COMPLETE current schema.
 *
 * Why this is needed: the Postgres bootstrap schema (scripts/build-postgres-schema.js)
 * is generated from server/db/schema.sql ONLY. But the app adds many columns/tables
 * at runtime via the migration list + multi-tenancy migration in server/db/database.js,
 * and those only ever ran against SQLite. So a migrated Postgres DB is missing them
 * (e.g. users.email_alerts, users.must_change_password, devices.workspace_id,
 * devices.sort_order, ...), which makes requireAuth and most queries fail.
 *
 * This script boots a throwaway SQLite DB (which applies schema.sql + ALL runtime
 * migrations), introspects the resulting complete schema, and emits:
 *   - CREATE TABLE IF NOT EXISTS  (for any table missing entirely)
 *   - ALTER TABLE ... ADD COLUMN IF NOT EXISTS  (for every column)
 * Both are no-ops where the object already exists, so it's safe to run repeatedly.
 *
 * Usage:
 *   node scripts/generate-postgres-catchup.js
 * Writes: server/db/postgres-catchup.sql  (paste into the Supabase SQL editor)
 */

const path = require('path');
const fs = require('fs');
const os = require('os');

const tmpDb = path.join(os.tmpdir(), `sf-catchup-${Date.now()}.db`);
process.env.DB_CLIENT = 'sqlite';
process.env.DB_PATH = tmpDb;

// Requiring database.js runs schema.sql + the full migration list + the
// multi-tenancy migration against the throwaway DB.
const { db: sqlite } = require(path.join(__dirname, '..', 'server', 'db', 'database'));

function pgType(sqliteType) {
  const t = String(sqliteType || '').toUpperCase();
  if (t.includes('INT')) return 'BIGINT';
  if (t.includes('REAL') || t.includes('FLOA') || t.includes('DOUB')) return 'DOUBLE PRECISION';
  if (t.includes('BLOB')) return 'BYTEA';
  if (t.includes('BOOL')) return 'BOOLEAN';
  if (t.includes('NUMERIC') || t.includes('DEC')) return 'NUMERIC';
  return 'TEXT'; // TEXT, CHAR, CLOB, and anything unrecognised
}

function pgDefault(dflt) {
  if (dflt === null || dflt === undefined) return '';
  const d = String(dflt).trim();
  if (/strftime\(\s*'%s'\s*,\s*'now'\s*\)/i.test(d)) return ' DEFAULT (EXTRACT(EPOCH FROM now())::bigint)';
  return ` DEFAULT ${d}`; // numeric or already-quoted string literal
}

const tables = sqlite.prepare(
  "SELECT name FROM sqlite_master WHERE type='table' AND name NOT LIKE 'sqlite_%' ORDER BY name"
).all().map(r => r.name);

const out = [];
out.push('-- Postgres catch-up migration generated by scripts/generate-postgres-catchup.js');
out.push('-- Brings a Supabase DB up to the complete runtime schema. Idempotent & safe to re-run.');
out.push('-- Paste into the Supabase SQL editor and run.');
out.push('');

for (const table of tables) {
  const cols = sqlite.prepare(`PRAGMA table_info("${table}")`).all();
  if (!cols.length) continue;
  out.push(`-- ${table}`);
  for (const c of cols) {
    out.push(`ALTER TABLE "${table}" ADD COLUMN IF NOT EXISTS "${c.name}" ${pgType(c.type)}${pgDefault(c.dflt_value)};`);
  }
  out.push('');
}

const outPath = path.join(__dirname, '..', 'server', 'db', 'postgres-catchup.sql');
fs.writeFileSync(outPath, out.join('\n'));
sqlite.close();
try { fs.unlinkSync(tmpDb); fs.unlinkSync(tmpDb + '-wal'); fs.unlinkSync(tmpDb + '-shm'); } catch {}
console.log(`Wrote ${path.relative(path.join(__dirname, '..'), outPath)} (${tables.length} tables).`);
